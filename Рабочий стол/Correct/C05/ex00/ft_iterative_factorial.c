int	ft_iterative_factorial(int nb)
{
	int i;
	int answ;

	if (nb < 0)
		return (0);
	else
	{
		answ = 1;
		i = 1;
		while (i <= nb)
		{
			answ *= i;
			i++;
		}
		return (answ);
	}
}
