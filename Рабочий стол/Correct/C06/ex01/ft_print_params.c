#include <unistd.h>

int	main(int ag, char **amd)
{
	int j;
	int i;

	(void)ag;
	i = 1;
	j = 0;
	while (amd[i])
	{
		while (amd[i][j])
			j++;
		write(1, amd[i], j);
		write(1, "\n", 1);
		j = 0;
		i++;
	}
	return (0);
}
