#include <unistd.h>

int	main(int agm, char **str)
{
	int j;
	int i;

	i = agm - 1;
	j = 0;
	while (i > 0)
	{
		while (str[i][j])
			j++;
		write(1, str[i], j);
		write(1, "\n", 1);
		j = 0;
		i--;
	}
	return (0);
}
