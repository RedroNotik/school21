int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int answ;

	i = 0;
	answ = 0;
	while (tab[i][0] != '\0')
	{
		if (f(tab[i]) != 0)
			answ++;
		i++;
	}
	return (answ);
}
